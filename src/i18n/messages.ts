export type I18nKey = KeyPaths<typeof messages.en>;

type Primitive = string | number | boolean | null | undefined;
type KeyPaths<T, Prefix extends string = ""> = T extends Primitive
  ? never
  : {
      [K in keyof T & string]: T[K] extends Primitive
        ? `${Prefix}${K}`
        : `${Prefix}${K}` | KeyPaths<T[K], `${Prefix}${K}.`>;
    }[keyof T & string];

